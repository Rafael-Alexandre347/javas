package Imovel;

public class Porta {
	private String cor;
	private String material;
	
	public Porta(String cor, String material) {
		this.cor = cor;
		this.material = material;
	}
	
	public String getCor() {
		return cor;
	}
	public String getMaterial() {
		return material;
	}
	
	
}
